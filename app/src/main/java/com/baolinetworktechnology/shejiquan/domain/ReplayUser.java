package com.baolinetworktechnology.shejiquan.domain;

public class ReplayUser {
	public String replyContents;
	public String replayUserName;
	public String replayUserLogo;

	public String getReplyContents() {
		return replyContents;
	}
	public void setReplyContents(String replyContents) {
		this.replyContents = replyContents;
	}
	public String getReplayUserName() {
		return replayUserName;
	}
	public void setReplayUserName(String replayUserName) {
		this.replayUserName = replayUserName;
	}
	public String getReplayUserLogo() {
		return replayUserLogo;
	}
	public void setReplayUserLogo(String replayUserLogo) {
		this.replayUserLogo = replayUserLogo;
	}
}
